    private void GetAllEntries(){        
        SearchEntries se = new SearchEntries();
        se.getAllEntries().stream().forEach((temp) -> {
            Box box = Box.createVerticalBox();
            jPanelEntries.add(box);
            jPanelEntries.add(Box.createRigidArea(new Dimension(10, 10)));
            box.setSize(380, 50);
            box.add(new JLabel(temp.getAddress()+", "+temp.getCity()+", "+temp.getCountry()));
            box.setBorder(BorderFactory.createLineBorder(Color.black));
        });
        
    }
    
