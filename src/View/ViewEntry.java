    public ViewEntry(Entry entry){
        initComponents();
        jPanelComments.setLayout(new GridLayout(0,1));
        comments.addAll(comm.GetComments(entry.getId()));
        this.entry = entry;
        jtxtTitle.setText(entry.getTitle());
        jtxtPrice.setText(entry.getPrice()+"â‚¬");
        jtxtAddress.setText(entry.getAddress()+", "+entry.getCity());
        jtxtCountry.setText(entry.getCountry());
        jtxtUser.setText(entry.getUser());
        
//        jPanel2.add(new JLabel(entry.getPhoto()));

        jtxtDescription.setText(String.format("<html><div style=\"width:%dpx;\">%s</div><html>", 100, entry.getDescription()));
        LoadComments();
        
        BufferedImage img = null;
        try {
            img = ImageIO.read(new File(entry.getPhoto()));
            
        } catch (IOException e) {
            e.printStackTrace();
        }
        
        Image dimg = img.getScaledInstance(100, 100, Image.SCALE_SMOOTH);
        ImageIcon imageIcon = new ImageIcon(dimg);
        jPanel2.add(new JLabel(imageIcon));
    }
    
    private void LoadComments(){
        comments.stream().forEach((Comment comment) -> {
            Box box = Box.createVerticalBox();
            box.add(Box.createRigidArea(new Dimension(5, 5)));
            FindUser fu = new FindUser(comment.getSenterId());
            jPanelComments.add(box);
            box.add(new JLabel("Posted by "+fu.GetUserName()));
            box.add(new JLabel(comment.getMessage()));
            box.add(new JLabel("________________________________________________"));
            
        });
        
        comments.clear();
        
    }
    
    private void RePaintComments(){
        jPanelComments.removeAll();
        LoadComments();
    }

